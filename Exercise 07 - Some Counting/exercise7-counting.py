# a loop that counts up from 0 to 50 in increments of 1.

for x in range(51) : # range starts at 0 and ends before 51, increments of 1
  print(x)

print(" ") # prints an empty line to separate the output of the loops.

# a loop that counts down from 50 to 0 in decrements of 1.

for x in range(50, -1, -1) : # range starts at value 50 and ends before the value -1, decrements of 1
  print(x)

print(" ") # prints an empty line to separate the output of the loops.

